public class Solution01 {
    public static void main(String[] args) {
        // main method-this is where java program begins its execution
        for(int i=10;i<=50;i++){
            // for-loop starts with i=10 and continues as long as i<= 50.
            // i will be incremented by 1 after each iteration
            System.out.print(i + " ");
            // print i value with space in same line
        }
    }
}
